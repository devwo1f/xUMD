import { addDays, addHours, addMinutes, startOfDay, subHours } from 'date-fns';
import { EventCategory, type Event } from '../../shared/types';

const now = new Date();
const todayStart = startOfDay(now);

function isoFromNow(hoursOffset: number, minuteOffset = 0) {
  return addMinutes(addHours(now, hoursOffset), minuteOffset).toISOString();
}

function isoFromDay(dayOffset: number, hour: number, minute = 0) {
  const date = addDays(todayStart, dayOffset);
  date.setHours(hour, minute, 0, 0);
  return date.toISOString();
}

function createEvent(event: Event): Event {
  return {
    ...event,
    status: event.status ?? 'upcoming',
    moderation_status: event.moderation_status ?? 'approved',
    attendee_count: event.attendee_count ?? event.rsvp_count,
    interested_count: event.interested_count ?? Math.max(12, Math.round(event.rsvp_count * 0.35)),
    organizer_name: event.organizer_name ?? 'xUMD',
    tags: event.tags ?? [],
  };
}

